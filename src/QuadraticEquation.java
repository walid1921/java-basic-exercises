
// Quadratic Equation

public class QuadraticEquation {
    public static void main(String[] args) {

        double num = 4;

        double result = 3 * (num * num) - (8 * num) + 4;
        // or double result = (3 * Math.pow(num, 2)) - (8 * num) + 4;

        System.out.println("The square of " + num + " is " + result);

    }

}
